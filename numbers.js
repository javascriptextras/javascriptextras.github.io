export function decimalAdd(des1, des2) {
  return (des1 * 10 + des2 * 10) / 10;
}

export function decimalSub(des1, des2) {
  return (des1 * 10 - des2 * 10) / 10;
}

export function decimalMul(des1, des2) {
  return (des1 * 10 * des2 * 10) / 10;
}

export function decimalDiv(des1, des2) {
  return (des1 * 10 / des2 * 10) / 10;
}
